 //-----------------------------  Print3d( )  ---------------------------//
 /************************************************************************/

 void Print3d(const int x,const int y,const char* Text,
				  const int text_color,const int direction)
    {
       HideMouse( );
       GetCurrentSettings( );

       settextstyle(0,direction,1);

       if(text_color==0)
	  setcolor(7);

       else
	 setcolor(0);

       outtextxy(x,(y+1),Text);

	 setcolor(text_color);
	   outtextxy((x+1),y,Text);

       RestorePreviousSettings( );
       ShowMouse( );
    }

 /************************************************************************/
 //----------------------------  Print3d( )  ----------------------------//
 /************************************************************************/

 void Print3d(const int x,const int y,const long double number,
				   const int text_color,const int direction)
    {
       HideMouse( );
       GetCurrentSettings( );

       char Number[30]={NULL};

       gcvt(number,8,Number);

       settextstyle(0,direction,1);

       if(text_color==0)
	  setcolor(7);

       else
	 setcolor(0);

       outtextxy(x,(y+1),Number);

	 setcolor(text_color);
	   outtextxy((x+1),y,Number);

       RestorePreviousSettings( );
       ShowMouse( );
    }

