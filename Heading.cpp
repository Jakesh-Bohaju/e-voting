 //-----------------------------  Heading( )  ---------------------------//
 /************************************************************************/

 void Heading(const int x,const int y,const char* Text,const int text_color,
				   const int font_size,const int font_style)
    {
       HideMouse( );
       GetCurrentSettings( );

       settextstyle(font_style,0,font_size);

       if(text_color==0)
	  setcolor(7);

       else
	 setcolor(0);

	   outtextxy((x-2),(y+3),Text);
	   outtextxy((x-2),(y+2),Text);
	   outtextxy((x-1),(y+2),Text);
	   outtextxy((x-1),(y+1),Text);
	   outtextxy((x-1),y,Text);
	   outtextxy(x,y,Text);

	 setcolor(text_color);
	   outtextxy(x,y,Text);
	   outtextxy((x+1),y,Text);
	   outtextxy((x+1),(y+1),Text);

       RestorePreviousSettings( );
       ShowMouse( );
    }

