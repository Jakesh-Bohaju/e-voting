 /************************************************************************/
 //---------------------------  Panel( )  -------------------------------//
 /************************************************************************/

 Panel::Panel( )
    {
       x_1=0;
       y_1=0;
       x_2=0;
       y_2=0;
       in_out=IN;
       thick_thin=THIN;
    }

 /************************************************************************/
 //-----------------------------  Init( )  ------------------------------//
 /************************************************************************/

 void Panel::Init(const int left,const int top,const int right,
				      const int bottom,const int IN_OUT,
				      const int THICK_THIN,const int color)
    {
       x_1=left;
       y_1=top;
       x_2=right;
       y_2=bottom;
       panel_color=color;
       in_out=IN_OUT;
       thick_thin=THICK_THIN;
    }

 /************************************************************************/
 //------------------------------  Show( )  -----------------------------//
 /************************************************************************/

 void Panel::Show( )
   {
       HideMouse( );
       GetCurrentSettings( );

       setfillstyle(1,panel_color);
	 bar(x_1,y_1,x_2,y_2);

       if(in_out==OUT)
	  setcolor(15);

       else
	  setcolor(8);

       line(x_1,y_1,(x_2-1),y_1);
       line(x_1,y_1,x_1,(y_2-1));

       if(thick_thin==THICK)
	  {
	     line((x_1+1),(y_1+1),x_2,(y_1+1));
	     line((x_1+1),y_1,(x_1+1),y_2);
	  }

       if(in_out==OUT)
	  setcolor(8);

       else
	  setcolor(15);

       line((x_1+1),y_2,x_2,y_2);
       line(x_2,(y_1+1),x_2,y_2);

       if(thick_thin==THICK)
	  {
	     line((x_1+2),(y_2-1),x_2,(y_2-1));
	     line((x_2-1),(y_1+2),(x_2-1),y_2);
	  }

       RestorePreviousSettings( );
       ShowMouse( );
    }

 /************************************************************************/
 //----------------------------  Hit( )  --------------------------------//
 /************************************************************************/

 const int Panel::Hit( )
    {
       int x=0;
       int y=0;

       GetMouseCoordinates(&x,&y);

       return ((x>=x_1 && x<=x_2 && y>=y_1 && y<=y_2)?1:0);
    }

